package loopspracticing.src;

public class WhileDoFor {
    public static void main(String[] args) {

        int i=1;

        while (i<5){System.out.println(i);i++;}



        System.out.println("Next");

        int a=1;

        do{System.out.println(a);a++;}
        while(a<5);



        System.out.println("Next");

        int b=1;
        for(b=1; b<5; b++) {
            System.out.println(b);}
    }
}
